.386
.model FLAT, C

PUBLIC P0
PUBLIC S1
PUBLIC S3
PUBLIC S

EXTERN SetCler : NEAR
EXTERN Count : NEAR
EXTERN A_u_B : NEAR
EXTERN A_B : NEAR

.DATA
S DD 0,0
S1 DD 3,0
S2 DD 11,0
S3 DD 11,0
S4 DD 3,0
L DD 38
N DD 6
P DD 1

.CODE
P0:
	PUSH P
	PUSH N
	PUSH OFFSET S
    CALL SetCler
	ADD ESP,12 ;очистка стека

	PUSH L
	PUSH OFFSET S1
	CALL Count
	ADD ESP, 8

	PUSH L
	PUSH OFFSET S2
	PUSH OFFSET S1
	CALL A_U_B
	ADD ESP,12

	PUSH L
	PUSH OFFSET S4
	PUSH OFFSET S3
	CALL A_B
	ADD ESP,12

	RET
END